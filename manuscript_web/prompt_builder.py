def _kst_now():
    kst = timezone(timedelta(hours=9))
    return datetime.now(kst).strftime("%Y-%m-%d %H:%M (KST)")


def build_system_prompt():
    """시스템 프롬프트(캐싱 대상). 지침 변경 전까지 고정."""
    return build_system_instruction()


